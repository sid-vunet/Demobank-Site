-- ============================================
-- Create Sequences
-- ============================================
CREATE SEQUENCE FIN_CUSTOMER_SEQ START WITH 1000001 INCREMENT BY 1;
CREATE SEQUENCE FIN_ACCOUNT_SEQ START WITH 5000001 INCREMENT BY 1;
CREATE SEQUENCE FIN_TRANSACTION_SEQ START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE FIN_AUDIT_SEQ START WITH 1 INCREMENT BY 1;

-- ============================================
-- Users Table
-- ============================================
CREATE TABLE FIN_USERS (
    USER_ID VARCHAR2(50) PRIMARY KEY,
    USERNAME VARCHAR2(100) NOT NULL,
    PASSWORD_HASH VARCHAR2(256) NOT NULL,
    FULL_NAME VARCHAR2(200),
    EMAIL VARCHAR2(200),
    PHONE VARCHAR2(20),
    ROLE VARCHAR2(50) DEFAULT 'USER',
    BRANCH_CODE VARCHAR2(20),
    IS_ACTIVE NUMBER(1) DEFAULT 1,
    LAST_LOGIN TIMESTAMP,
    CREATED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- ============================================
-- Customers Table (CIF - Customer Information File)
-- ============================================
CREATE TABLE FIN_CUSTOMERS (
    CUSTOMER_ID VARCHAR2(20) PRIMARY KEY,
    CUSTOMER_TYPE VARCHAR2(20) NOT NULL, -- RETAIL, CORPORATE
    FULL_NAME VARCHAR2(200) NOT NULL,
    DATE_OF_BIRTH DATE,
    GENDER VARCHAR2(10),
    EMAIL VARCHAR2(200),
    PHONE VARCHAR2(20),
    MOBILE VARCHAR2(20),
    ADDRESS_LINE1 VARCHAR2(300),
    ADDRESS_LINE2 VARCHAR2(300),
    CITY VARCHAR2(100),
    STATE VARCHAR2(100),
    PIN_CODE VARCHAR2(10),
    COUNTRY VARCHAR2(100) DEFAULT 'INDIA',
    ID_TYPE VARCHAR2(50), -- AADHAR, PAN, PASSPORT, VOTER_ID
    ID_NUMBER VARCHAR2(50),
    PAN_NUMBER VARCHAR2(20),
    AADHAR_NUMBER VARCHAR2(20),
    OCCUPATION VARCHAR2(100),
    ANNUAL_INCOME NUMBER(15,2),
    RELATIONSHIP_MANAGER VARCHAR2(50),
    KYC_STATUS VARCHAR2(20) DEFAULT 'PENDING', -- PENDING, VERIFIED, REJECTED
    RISK_CATEGORY VARCHAR2(20) DEFAULT 'LOW', -- LOW, MEDIUM, HIGH
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE', -- ACTIVE, INACTIVE, BLOCKED, CLOSED
    CREATED_BY VARCHAR2(50),
    CREATED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_BY VARCHAR2(50),
    UPDATED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    APPROVED_BY VARCHAR2(50),
    APPROVED_DATE TIMESTAMP
);

-- ============================================
-- Accounts Table
-- ============================================
CREATE TABLE FIN_ACCOUNTS (
    ACCOUNT_ID VARCHAR2(20) PRIMARY KEY,
    CUSTOMER_ID VARCHAR2(20) NOT NULL,
    ACCOUNT_TYPE VARCHAR2(30) NOT NULL, -- SAVINGS, CURRENT, FD, RD
    ACCOUNT_NAME VARCHAR2(200),
    CURRENCY VARCHAR2(10) DEFAULT 'INR',
    BALANCE NUMBER(18,2) DEFAULT 0,
    AVAILABLE_BALANCE NUMBER(18,2) DEFAULT 0,
    INTEREST_RATE NUMBER(5,2),
    OPENING_DATE DATE DEFAULT SYSDATE,
    MATURITY_DATE DATE,
    BRANCH_CODE VARCHAR2(20),
    IFSC_CODE VARCHAR2(20),
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE', -- ACTIVE, DORMANT, FROZEN, CLOSED
    NOMINEE_NAME VARCHAR2(200),
    NOMINEE_RELATION VARCHAR2(50),
    CREATED_BY VARCHAR2(50),
    CREATED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_BY VARCHAR2(50),
    UPDATED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_ACC_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES FIN_CUSTOMERS(CUSTOMER_ID)
);

-- ============================================
-- Transactions Table
-- ============================================
CREATE TABLE FIN_TRANSACTIONS (
    TRANSACTION_ID VARCHAR2(30) PRIMARY KEY,
    ACCOUNT_ID VARCHAR2(20) NOT NULL,
    TRANSACTION_TYPE VARCHAR2(30) NOT NULL, -- CREDIT, DEBIT, TRANSFER, DEPOSIT, WITHDRAWAL
    TRANSACTION_MODE VARCHAR2(30), -- CASH, CHEQUE, NEFT, RTGS, IMPS, UPI
    AMOUNT NUMBER(18,2) NOT NULL,
    BALANCE_AFTER NUMBER(18,2),
    DESCRIPTION VARCHAR2(500),
    REFERENCE_NO VARCHAR2(50),
    TO_ACCOUNT_ID VARCHAR2(20),
    TO_IFSC VARCHAR2(20),
    TO_ACCOUNT_NAME VARCHAR2(200),
    STATUS VARCHAR2(20) DEFAULT 'SUCCESS', -- SUCCESS, PENDING, FAILED, REVERSED
    TRANSACTION_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    VALUE_DATE DATE DEFAULT SYSDATE,
    BRANCH_CODE VARCHAR2(20),
    TELLER_ID VARCHAR2(50),
    REMARKS VARCHAR2(500),
    CREATED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_TXN_ACCOUNT FOREIGN KEY (ACCOUNT_ID) REFERENCES FIN_ACCOUNTS(ACCOUNT_ID)
);

-- ============================================
-- Audit Logs Table
-- ============================================
CREATE TABLE FIN_AUDIT_LOGS (
    AUDIT_ID NUMBER PRIMARY KEY,
    ENTITY_TYPE VARCHAR2(50) NOT NULL, -- CUSTOMER, ACCOUNT, TRANSACTION, USER
    ENTITY_ID VARCHAR2(50) NOT NULL,
    ACTION VARCHAR2(50) NOT NULL, -- CREATE, UPDATE, DELETE, VIEW, LOGIN, LOGOUT
    OLD_VALUE CLOB,
    NEW_VALUE CLOB,
    FIELD_NAME VARCHAR2(100),
    USER_ID VARCHAR2(50),
    USER_IP VARCHAR2(50),
    USER_AGENT VARCHAR2(500),
    SESSION_ID VARCHAR2(100),
    ACTION_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    REMARKS VARCHAR2(500)
);

-- ============================================
-- Menu Items Table (Dynamic Menu Configuration)
-- ============================================
CREATE TABLE FIN_MENU_ITEMS (
    MENU_ID VARCHAR2(50) PRIMARY KEY,
    PARENT_ID VARCHAR2(50),
    MENU_NAME VARCHAR2(100) NOT NULL,
    MENU_ICON VARCHAR2(50),
    FUNCTION_ID VARCHAR2(50),
    MENU_ORDER NUMBER DEFAULT 0,
    IS_FOLDER NUMBER(1) DEFAULT 0,
    IS_ACTIVE NUMBER(1) DEFAULT 1,
    ROLE_REQUIRED VARCHAR2(100),
    CREATED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- ============================================
-- Create Indexes for Performance
-- ============================================
CREATE INDEX IDX_CUSTOMER_TYPE ON FIN_CUSTOMERS(CUSTOMER_TYPE);
CREATE INDEX IDX_CUSTOMER_STATUS ON FIN_CUSTOMERS(STATUS);
CREATE INDEX IDX_CUSTOMER_NAME ON FIN_CUSTOMERS(FULL_NAME);
CREATE INDEX IDX_CUSTOMER_PHONE ON FIN_CUSTOMERS(MOBILE);
CREATE INDEX IDX_CUSTOMER_PAN ON FIN_CUSTOMERS(PAN_NUMBER);
CREATE INDEX IDX_CUSTOMER_AADHAR ON FIN_CUSTOMERS(AADHAR_NUMBER);

CREATE INDEX IDX_ACCOUNT_CUSTOMER ON FIN_ACCOUNTS(CUSTOMER_ID);
CREATE INDEX IDX_ACCOUNT_TYPE ON FIN_ACCOUNTS(ACCOUNT_TYPE);
CREATE INDEX IDX_ACCOUNT_STATUS ON FIN_ACCOUNTS(STATUS);

CREATE INDEX IDX_TXN_ACCOUNT ON FIN_TRANSACTIONS(ACCOUNT_ID);
CREATE INDEX IDX_TXN_DATE ON FIN_TRANSACTIONS(TRANSACTION_DATE);
CREATE INDEX IDX_TXN_TYPE ON FIN_TRANSACTIONS(TRANSACTION_TYPE);
CREATE INDEX IDX_TXN_STATUS ON FIN_TRANSACTIONS(STATUS);

CREATE INDEX IDX_AUDIT_ENTITY ON FIN_AUDIT_LOGS(ENTITY_TYPE, ENTITY_ID);
CREATE INDEX IDX_AUDIT_DATE ON FIN_AUDIT_LOGS(ACTION_DATE);
CREATE INDEX IDX_AUDIT_USER ON FIN_AUDIT_LOGS(USER_ID);

